    function validateCreditCard() {
        var ccnumber = $('#ccnumber').val();
        var payment = $('#payment').val();
        
        var b_error = false;
        var cart_type = "visa,mastercard,visa_electron,maestro,amex,discover,diners_club_international,diners_club_carte_blanche,jcb,laser";
        
        if(in_array(payment, credit_cards)) {
            if(ccnumber == '') {
                $('#ccnumber').addClass('errorInput');
                $('#ccnumber_msg').html("Card number code can't be empty.");
                b_error = true;
            } else {
                $('#ccnumber').validateCreditCard(function(result) {
                    if(result.card_type == null) {
                        ccnumberInvalid();
                        b_error = true;
                    } else {
                        if(result.luhn_valid == false) {
                            ccnumberInvalid();
                            b_error = true;
                        } else if(result.length_valid == false) {
                            ccnumberInvalid();
                            b_error = true;
                        } else {
                            var card_type_select = result.card_type.name;
                            var cart_types = cart_type.split(",");

                            if(in_array(card_type_select, cart_types)) {
                                $("#card_type").val(card_type_select);
                                $('#ccnumber').removeClass('errorInput');
                                $('#ccnumber_msg').html("");
                            } else {
                                b_error = true;
                                $('#ccnumber').addClass('errorInput');
                                $('#ccnumber_msg').html("Payment and card number does not match!");
                            }
                        }
                    }
                });
            }
        }
        
        return b_error;
    }
    
